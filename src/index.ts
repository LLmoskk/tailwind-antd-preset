// Generate color object for Tailwind
function generateColorObject(colors: Record<string, string[]>, prefix: string = '') {
  const result: Record<string, Record<string, string>> = {};
  
  Object.entries(colors).forEach(([colorName, shades]) => {
    const colorKey = prefix ? `${prefix}-${colorName}` : colorName;
    result[colorKey] = {};
    
    shades.forEach((shade, index) => {
      result[colorKey][String(index + 1)] = shade;
    });
  });
  
  return result;
}

// Generate primary color object
function generatePrimaryColors(colors: Record<string, string>, prefix: string = '') {
  const result: Record<string, string> = {};
  
  Object.entries(colors).forEach(([colorName, color]) => {
    const colorKey = prefix ? `${prefix}-${colorName}` : colorName;
    result[colorKey] = color;
  });
  
  return result;
}

